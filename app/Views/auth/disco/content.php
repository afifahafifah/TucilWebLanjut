    <h5 class="card-title text-center">Royal Canin</h5>
    <p class="card-text">Bagi para pecinta kucing dan anjing, nama Royal Canin tidaklah asing lagi. Royal Canin merupakan salah satu merk makanan anjing dan kucing premium yang terkenal di seluruh dunia.</p>

    Apa yang membuat Royal Canin berbeda dari merk makanan hewan lainnya? Karena Royal Canin percaya bahwa setiap hewan adalah unik, dan setiap hewan memiliki kebutuhan yang berbeda.</p>

    Royal Canin didirikan oleh seorang dokter hewan Jean Cathary di Perancis tahun 1968. Dengan latar belakang seorang dokter hewan, Jean Cathary tentunya memiliki banyak pengetahuan tentang hewan dan melihat peluang untuk membuat makanan anjing dan kucing kualitas premium yang disesuaikan dengan kebutuhan hewan peliharaan serta kepuasan dari pemilik hewan tersebut.</p>
    
    Berbagai penelitian dilakukan untuk menganalisa kebutuhan nutrisi setiap jenis anjing dan kucing, lalu dikombinasikan dengan postur tubuh serta kebiasaan setiap hewan. Karena setiap hewan peliharaan itu berbeda, dilihat dari jenisnya, ukuran tubuh, bentuk rahang, usia, dan aktivitasnya.</n>

    Tak hanya itu saja, Royal Canin juga menyediakan berbagai makanan anjing dan makanan kucing bagi hewan peliharaan dengan penyakit tertentu. Maka tak heran jika mereka Royal Canin direkomendasikan oleh banyak dokter hewan.</p>
